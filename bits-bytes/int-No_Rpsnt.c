#include <stdio.h>

int main() {
  int i;

  printf("i = 256, d displayed i = %d\n", i = 256);
  printf("i = 256, x displayed i = %x\n", i = 256);
  printf("i = 256, i displayed i = %i\n", i = 256);
  printf("i = 256, u displayed i = %u\n", i = 256);
  printf("i = 256, o displayed i = %o\n", i = 256);
  printf("\ni = -256, x displayed i = %x\n", i = -256);

  return 0;
}
